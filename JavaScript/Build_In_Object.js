// ------------------------------
// Math Constants and Functions
// ------------------------------

//  PI constant (ratio of circumference to diameter of a circle)
console.log("Math.PI:", Math.PI);  // Output: 3.141592653589793

//  Math.max() returns the largest number
console.log("Math.max(2, 34, 54, 323, 32):", Math.max(2, 34, 54, 323, 32));  // Output: 323

//  Math.min() returns the smallest number
console.log("Math.min(21, 32, 12, 3, 2, 4, 12):", Math.min(21, 32, 12, 3, 2, 4, 12));  // Output: 2

//  Math.round() rounds to the nearest integer
console.log("Math.round(5.5):", Math.round(5.5));  // Output: 6

//  Math.floor() always rounds down to the nearest integer
console.log("Math.floor(1.6):", Math.floor(1.6));  // Output: 1

//  Math.ceil() always rounds up to the nearest integer
console.log("Math.ceil(1.3):", Math.ceil(1.3));  // Output: 2

//  Math.abs() returns the absolute (positive) value
console.log("Math.abs(-5):", Math.abs(-5));  // Output: 5

//  Math.random() generates a random decimal between 0 (inclusive) and 1 (exclusive)
console.log("Math.random():", Math.random());  // Output: e.g., 0.7382

//  Random number between 1 and 100 (non-integer)
console.log("Math.random() * 100 + 1:", Math.random() * 100 + 1);  // e.g., 47.982

//  Math.sqrt() returns the square root
console.log("Math.sqrt(25):", Math.sqrt(25));  // Output: 5

//  Math.pow(x, y) returns x raised to the power y
console.log("Math.pow(2, 8):", Math.pow(2, 8));  // Output: 256
